ll n, k, boss, ans = 0;
vi f[N];
bool M[N];

void dfs(int u, int par1, int par2) {
    if(par1 != -1 && abs(par1 - u) <= k) {
        cout << par1 << " " << u << '\n';
        ans++;
    }
    if(par2 != -1 && abs(par2 - u) <= k) {
        cout << par2 << " " << u << '\n';
        ans++;
    }
    for(int j = 0, i; j < f[u].size(); j++) {
        i = f[u][j];
        dfs(i, u, par1);
    }
}

void solve() {
    dfs(boss, -1, -1);
    cout << ans;
}

void init() {
    cin >> n >> k;
    for(int i = 1; i < n; i++) {
        int u, v;
        cin >> u >> v;
        f[u].pb(v);
        if(!M[u])
            boss = u;
        M[v] = 1;
    }
}

